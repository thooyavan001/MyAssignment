package week2.day1;

public class ForLoop {
	public static void main(String[] args) {
		for (int i=0; i<=15;i++) {//incremental method
			System.out.println(i);
		}
		for (int i=5;i>0;i--) {//decremental method
			System.out.println(i);
		}
		for (int i=0;i<=20;i++) {
			if(i%2== 0) {
			System.out.println(i);}
		}
	}

}
